        jTextArea1ticket.append(formatoFecha.format(Calendar.getInstance().getTime()));     
        //TEXTO
        jTextArea1ticket.append("\n");
        //Mostrar hora de bajada de bandera
        DateFormat formatoHorabajada = DateFormat.getTimeInstance(DateFormat.MEDIUM);       
        //TEXTO
        jTextArea1ticket.append("Hora bajada Bandera: ");
        jTextArea1ticket.append(formatoHorabajada.format(Calendar.getInstance().getTime()));
        //Contador de tiempo START
        duracion.reset();
        duracion.start();
