    private void jButton1cancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1cancelarActionPerformed
       
        //Variable  
        
        String contadorDato;
        double tarifaMinuto = 0.50;
        
        
        
        //Sumar contador
        contador++;       
        //Dar formato al número del contador
        DecimalFormat formatoNumero = new DecimalFormat("0000");
        contadorDato = formatoNumero.format(contador);      
        //TEXTO
        jTextArea1ticket.append("\nNº Ticket: " + contadorDato);
        //Mostrar hora fin de trayecto
        DateFormat formatoHorafin = DateFormat.getTimeInstance(DateFormat.MEDIUM);       
        //TEXTO
        jTextArea1ticket.append("\nHora fin de trayecto: ");
        jTextArea1ticket.append(formatoHorafin.format(Calendar.getInstance().getTime()));
        //Duración de Trayecto
        jTextArea1ticket.append("\nDuración trayecto: ");
        //Contador de tiempo STOP y RESET
        duracion.stop();
        //Dar el tiempo
        tiempoMLS = duracion.getTime();
        duracionFormato = DurationFormatUtils.formatDurationHMS(tiempoMLS);
        jTextArea1ticket.append(duracionFormato);
        //TEXTO
        jTextArea1ticket.append("\nTarifa por minuto: ");
        //Tarifa por minuto
        NumberFormat formatoTarifaMinuto;
        formatoTarifaMinuto = NumberFormat.getCurrencyInstance(new Locale("es", "ES"));
        jTextArea1ticket.append(formatoTarifaMinuto.format(tarifaMinuto));
        //TEXTO
        jTextArea1ticket.append("\n");
        jTextArea1ticket.append("\n");
        //TEXTO
        NumberFormat formatoEuro = NumberFormat.getCurrencyInstance(new Locale("es","ES"));
        double importe = (tiempoMLS/60000)*tarifaMinuto;
        double iva = ((21 * importe) / 100);
        double importeTotal = iva + importe;
        jTextArea1ticket.append("Importe: " + String.valueOf(formatoEuro.format(importe)));
        jTextArea1ticket.append("\n");
        //IVA
        NumberFormat formatoIva;
        formatoIva = NumberFormat.getCurrencyInstance(new Locale("es", "ES"));
        jTextArea1ticket.append("I.V.A: " + formatoEuro.format(iva));
        jTextArea1ticket.append("\n");
        //TEXTO
        jTextArea1ticket.append("Importe total: " + formatoEuro.format(importeTotal));
        

    }//GEN-LAST:event_jButton1cancelarActionPerformed

